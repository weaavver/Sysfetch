    public void search(File dir, String keyword, ArrayList<File> results){
        File[] files = dir.listFiles();
        if(files == null){
            
            return;
        }
        
        for(File f : files){
            if(f.getName().toLowerCase().contains(keyword.toLowerCase())){
                results.add(f);
            }
            
            if(f.isDirectory()){ 
                search(f, keyword, results);
            }
        }
    }
    
    public void LoadDir(String path){
        currentPath = path;
        
        File directory = new File(path);
        File[] files = directory.listFiles();
        if(files == null) return;
        
        DefaultTableModel model = (DefaultTableModel) FileBrowser.getModel();
        model.setRowCount(0);
        
        for(File f : files){
            
            //String name = f.isFile() ? f.getName() : "📁 " + f.getName();
            String name = f.getName();
            String size = f.isFile() ? String.valueOf(f.length()) + " bytes" : "";
            String type = f.isDirectory() ? "Folder" : "File";
            String fullpath = f.getAbsolutePath();
            
            model.addRow(new Object[]{
                name, size, type, fullpath
            });
        }
        
        txtPATH.setText(path);
        
        //DOESNT CHANGE SIZE FOR SOME REASON
        FileBrowser.getColumnModel().getColumn(0).setPreferredWidth(200); //NAME
        FileBrowser.getColumnModel().getColumn(1).setPreferredWidth(1); //SIZE
        FileBrowser.getColumnModel().getColumn(2).setPreferredWidth(3); //TYPE
        FileBrowser.getColumnModel().getColumn(3).setPreferredWidth(1); //PATH
    }
    
