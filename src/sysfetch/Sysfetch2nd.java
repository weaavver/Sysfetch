    private void FileBrowserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FileBrowserMouseClicked
        if(evt.getClickCount() == 2){     //2 for double click
            int row = FileBrowser.getSelectedRow();
            if(row == -1) return; //for error handling
            
            //You can use name: String name = FileBrowser.getValueAt(row, 0).toString(); //gets the folder name
            String type = FileBrowser.getValueAt(row, 2).toString(); //gets the folder type
            String fullpath = FileBrowser.getValueAt(row, 3).toString();
            
            if(type.equals("Folder")){
                // like this: String newPath = currentPath + File.separator + name;//if it is folder then we change directory.
                history.push(currentPath);
                LoadDir(fullpath);
            }
        }
    }//GEN-LAST:event_FileBrowserMouseClicked
        boolean SearchMode;
    private void btnPrevDirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrevDirActionPerformed
        if(SearchMode){
            SearchMode = false;
            LoadDir(currentPath);
            return;
        }
        
        if(!history.isEmpty()){
            String prevPath = history.pop();
            LoadDir(prevPath);
        }
    }//GEN-LAST:event_btnPrevDirActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String text = txtSearch.getText();
        if(!text.isEmpty()){
            String keyword = txtSearch.getText();
            ArrayList<File> results = new ArrayList<>();

            SearchMode = true; //to prevent bug where the back button dont work if theres no result in search

            search(new File(currentPath), keyword, results);
            txtPATH.setText("");
            DefaultTableModel model = (DefaultTableModel) FileBrowser.getModel();
            model.setRowCount(0);

            if(results.isEmpty()){
                //only show one message if theres no output from search
                model.addRow(new Object[]{"No results found", "", "", ""});
            }

            for(File f : results){
                String name = f.getName();
                String size = f.isFile() ? f.length() + " bytes" : "";
                String type = f.isDirectory() ? "Folder" : "File";
                String fullpath = f.getAbsolutePath();

                model.addRow(new Object[]{name, size, type, fullpath});
            }
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnCreateDirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateDirActionPerformed
        String name = JOptionPane.showInputDialog(this, "Enter folder name");
        
        if(name != null && !name.isEmpty()){
            File folder = new File(currentPath + File.separator + name);
            
            if(folder.mkdir()){
                LoadDir(currentPath);
            }
            else{
                JOptionPane.showMessageDialog(this, "Failed to create folder");
            }
        }
    }//GEN-LAST:event_btnCreateDirActionPerformed

    private void btnCreateFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateFileActionPerformed
        String name = JOptionPane.showInputDialog(this, "Enter file name: ");
        
        if(name != null & !name.isEmpty()){
            File file = new File(currentPath + File.separator + name);
            
            try{
                if(file.createNewFile()){
                    LoadDir(currentPath);
                }
                else{
                    JOptionPane.showMessageDialog(this, "File already exists");
                }
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnCreateFileActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = FileBrowser.getSelectedRow();
        if(row == -1) return;
        
        String path = FileBrowser.getValueAt(row, 3).toString();
        File file = new File(path);
        
        int confirm = JOptionPane.showConfirmDialog(
                this, 
                "Delete" + file.getName() + "?",
                "Confirm",
                JOptionPane.YES_NO_OPTION
        );
        
        if(confirm == JOptionPane.YES_OPTION){
            if (file.delete()){
                LoadDir(currentPath);
            }
            else{
                JOptionPane.showMessageDialog(this, "Delete failed");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnRenameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRenameActionPerformed
        int row = FileBrowser.getSelectedRow();
        if(row == -1) return;
        
        String oldPath = FileBrowser.getValueAt(row, 3).toString();
        File oldFile = new File(oldPath);
        
        String newName = JOptionPane.showInputDialog(this, "Enter new name: ");
        
        if(newName != null && !newName.isEmpty()){
            File newFile = new File(oldFile.getParent() + File.separator + newName);
            
            if (oldFile.renameTo(newFile)){
                LoadDir(currentPath);
            }
            else{
                JOptionPane.showMessageDialog(this, "Rename failed");
            }
        }
    }//GEN-LAST:event_btnRenameActionPerformed

